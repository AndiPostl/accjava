DROP TABLE vets CASCADE CONSTRAINTS;
CREATE TABLE vets (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name VARCHAR(30),
  last_name VARCHAR(30)
);

DROP TABLE specialties CASCADE CONSTRAINTS;
CREATE TABLE specialties (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80)
);

DROP TABLE vet_specialties CASCADE CONSTRAINTS;
CREATE TABLE vet_specialties (
  vet_id INT NOT NULL,
  specialty_id INT NOT NULL,
  FOREIGN KEY (vet_id) REFERENCES vets(id),
  FOREIGN KEY (specialty_id) REFERENCES specialties(id),
  UNIQUE (vet_id,specialty_id)
);


DROP TABLE types CASCADE CONSTRAINTS;
CREATE TABLE types (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80)
);
CREATE INDEX types_name ON types(name);

DROP TABLE owners CASCADE CONSTRAINTS;
CREATE TABLE owners (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name VARCHAR(30),
  last_name VARCHAR(30),
  address VARCHAR(255),
  city VARCHAR(80),
  telephone VARCHAR(20)
);
CREATE INDEX owners_last_name ON owners(last_name);


DROP TABLE pets CASCADE CONSTRAINTS;
CREATE TABLE pets (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(30),
  birth_date DATE,
  type_id INT NOT NULL,
  owner_id INT NOT NULL,
  FOREIGN KEY (owner_id) REFERENCES owners(id),
  FOREIGN KEY (type_id) REFERENCES types(id)
);
CREATE INDEX pets_name ON pets(name);


DROP TABLE visits CASCADE CONSTRAINTS;
CREATE TABLE visits (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pet_id INT  NOT NULL,
  visit_date DATE,
  description VARCHAR(255),
  FOREIGN KEY (pet_id) REFERENCES pets(id)
);